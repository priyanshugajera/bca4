public class Sumbinary
{
	public static void main(String args[])
	{
		int binarysum=0b1100100+0b1100110;  //Binary Prefix ob
		int octalLsum=0144+0146;  //octal prefix 0
		int hexasum=0x65+0x66; //hexa prefix ox
		int decimalsum=100+100;

		System.out.println("binary sum:" + binarysum);
		System.out.println("octal sum:" + octalLsum);
		System.out.println("hexa sum:" + hexasum);
		System.out.println("decimal sum:" + decimalsum);	
	}
}